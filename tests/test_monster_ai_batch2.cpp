    global_rng().reseed(1);

    Entity bird;  bird.obj_type = ObjType::Bird;  bird.spr_num = 40;
    bird.bird_heights = {60, 70, 80, 90};
    Entity chimp5; chimp5.obj_type = ObjType::ChimpL5;
    chimp5.x = 200; chimp5.y = 100;
    Entity cl7; cl7.obj_type = ObjType::ChimpL7;
    cl7.x = 50; cl7.y = 220; cl7.state = 1; cl7.dy = 18;
    cl7.anchor_a = 210; cl7.anchor_b = 80;
    Entity ptero; ptero.obj_type = ObjType::PteriyakiL7;
    ptero.x = 100; ptero.wing_x = 140;
    ptero.anchor_a = 200; ptero.anchor_b = 60;
    Entity jf; jf.obj_type = ObjType::JumpingFishL5;
    jf.x = 100; jf.y = 220; jf.state = 1; jf.dy = 18; jf.direction = 1;
    Entity fire; fire.obj_type = ObjType::Fire; fire.spr_num = 105;
    Entity spider; spider.obj_type = ObjType::CaveSpider;
    Entity snake; snake.obj_type = ObjType::SnakeL3;
    snake.x = 80; snake.y = 110; snake.counter = 8;
    Entity afood; afood.obj_type = ObjType::AnimatedFoodL3; afood.counter = 12;
    Entity proj; proj.obj_type = ObjType::ProjectileL3;
    proj.init_x = 160; proj.init_y = 30;
    Entity l7a; l7a.obj_type = ObjType::MonsterL7A;
    l7a.x = 240; l7a.y = 120; l7a.init_x = 240; l7a.init_y = 120;
    l7a.spr_num = 44; l7a.init_spr = 42; l7a.away_spr = 46; l7a.ko_spr = 48;
